# Définition de la fonction hello
# Cette fonction ne prend aucun argument et retourne simplement "Hello!"
def hello
  "Hello!"
end

# Définition de la fonction greet
# Cette fonction prend un argument 'name' (une chaîne de caractères)
# et retourne "Hello, <name>!" en interpolant la valeur passée.
def greet(name)
  "Hello, #{name}!"
end

